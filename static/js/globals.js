const LANGUAGE_STORAGE_KEY = "doc-platform.language";
const SUPPORTED_LANGUAGES = new Set(["en", "ja"]);
const LANGUAGE_LOCALES = {
  en: "en-US",
  ja: "ja-JP",
};

// Language bootstrap — defined here because the `state` object below
// initializes `language: getInitialLanguage()` at load time, before any
// later script file (i18n.js) has executed.
function getInitialLanguage() {
  const stored = getStoredLanguage();
  if (stored) {
    return stored;
  }
  return window.navigator.language?.toLowerCase().startsWith("ja") ? "ja" : "en";
}

function getStoredLanguage() {
  try {
    const stored = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return SUPPORTED_LANGUAGES.has(stored) ? stored : null;
  } catch (error) {
    return null;
  }
}
